/**
 * 의성어/의태어 통합 사전
 */
export const koOnomatopoeia: Record<string, string> = {
  ...onomatopoeia,
  ...mimetics,
};

/**
 * 의성어/의태어 목록 (긴 것부터 정렬)
 */
export const onomatopoeiaList = Object.keys(koOnomatopoeia).sort((a, b) => b.length - a.length);

/**
 * 문장에서 의성어/의태어 번역
 */
export function translateOnomatopoeia(text: string): string {
  let result = text;

  for (const word of onomatopoeiaList) {
    if (result.includes(word)) {
      const translation = koOnomatopoeia[word];
      if (translation) {
        result = result.replace(new RegExp(word, 'g'), translation);
      }
    }
  }

  return result;
}
